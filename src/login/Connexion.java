    private void jButton_validerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_validerActionPerformed
         try{
            String uname1 = login.getText();
            String pass1 = String.valueOf(passe.getText());
            //url pour charger le driver de mysql
              String url = "jdbc:mysql://localhost:3306/pharmabase?autoReconnect=true&useSSL=false";
            //nom de l'utulisateur du base de donnees
            String user = "root";
            //Mot de pass du base de donnnes
            String password = "siby";
            //connection au base de donn√©e
            Connection con = DriverManager.getConnection(url, user, password);
            //requette
            String query = "SELECT * FROM `connexion` WHERE `login` = '"+uname1+"' AND `password` ='"+pass1+"'";
            //creation de la requette (Statement)
            Statement stm = con.createStatement();
            //exution de la requete
            ResultSet res = stm.executeQuery(query);

            if(res.next())
            {
                JOptionPane.showMessageDialog(null, "Connexion Reussi", "Connexion Avec succes", 1);
                //home mf = new home();
               

            }
            else{
                JOptionPane.showMessageDialog(null, " login ou Mot de pass Incorrect", "Echec", 2);
            }

        }catch(SQLException x){
            x.printStackTrace();
        }
    }//GEN-LAST:event_jButton_validerActionPerformed

